	PS.color(0, 12, PS.COLOR_RED);
	PS.color(1, 12, PS.COLOR_ORANGE);
	PS.color(2, 12, PS.COLOR_YELLOW);
	PS.color(3, 12, 0x239630); // dark green
	PS.color(4, 12, 0x457DD6); // blue
